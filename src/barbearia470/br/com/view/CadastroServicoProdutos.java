    //Preenche lista
    public void preencheTabelaServico() {

        String sql = "select *from Servicos";
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID", "Serviço", "Preço"};

        Connection conn = conexao.conectar();

        try (Statement stmt = conn.createStatement();
                ResultSet rs = stmt.executeQuery(sql)) {

            while (rs.next()) {

                
                CurrencyUnit real = CurrencyUnit.of("BRL");
                Money valor = Money.of(real, rs.getBigDecimal("preco"));
                
                dados.add(new Object[]{
                    rs.getInt("idServico"),
                    rs.getString("servico"),
                    valor
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

        ModeloTabela modelo = new ModeloTabela(dados, colunas);

        jTable_tabelaPesquisa.setModel(modelo);
        jTable_tabelaPesquisa.setFont(new java.awt.Font("Arial", 0, 16));

        jTable_tabelaPesquisa.getColumnModel().getColumn(0).setPreferredWidth(30);
        jTable_tabelaPesquisa.getColumnModel().getColumn(0).setResizable(false);

        jTable_tabelaPesquisa.getColumnModel().getColumn(1).setPreferredWidth(250);
        jTable_tabelaPesquisa.getColumnModel().getColumn(1).setResizable(false);

        jTable_tabelaPesquisa.getColumnModel().getColumn(2).setPreferredWidth(254);
        jTable_tabelaPesquisa.getColumnModel().getColumn(2).setResizable(false);

        jTable_tabelaPesquisa.getTableHeader().setReorderingAllowed(false);
        jTable_tabelaPesquisa.setAutoResizeMode(jTable_tabelaPesquisa.AUTO_RESIZE_ALL_COLUMNS);

        jTable_tabelaPesquisa.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        conexao.desconectar();

    }

