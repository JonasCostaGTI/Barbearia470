    private void jTable_tabelaPesquisaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_tabelaPesquisaMouseClicked

        jButton_editarServico.setEnabled(true);
        habilitaCamposServico();

        Conexao conexao = new Conexao();
        Connection conn = conexao.conectar();

        String idServico = "" + jTable_tabelaPesquisa.getValueAt(jTable_tabelaPesquisa.getSelectedRow(), 0);//0 == coluna da tabela
        String sql = "select *from Servicos where idServico = '" + idServico + "' ";

        try (Statement stmt = conn.createStatement();
                ResultSet rs = stmt.executeQuery(sql)) {

            if (rs.next()) {
                jTextField_idServico.setText(String.valueOf(rs.getInt("idServico")));
                jTextField_servico.setText(rs.getString("servico"));

                
                CurrencyUnit real = CurrencyUnit.of("BRL");
                Money valor = Money.of(real, rs.getBigDecimal("preco"));
                jFormattedTextField_valorServico.setText(valor.getAmount().toString().replace(".", ","));

                jFormattedTextField_valorServico.setEnabled(true);
                jTextField_servico.setEnabled(true);

            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

        conexao.desconectar();

    }//GEN-LAST:event_jTable_tabelaPesquisaMouseClicked

