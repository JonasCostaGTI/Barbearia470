    private void preencheTabelaProduto() {
        
        String sql = "select *from Produtos";
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID", "Item", "Preço"};

        Conexao conexao = new Conexao();
        Connection conn = conexao.conectar();

        try (Statement stmt = conn.createStatement();
                ResultSet rs = stmt.executeQuery(sql)) {

            while (rs.next()) {

                
                CurrencyUnit real = CurrencyUnit.of("BRL");
                Money preco = Money.of(real, rs.getBigDecimal("preco"));

                dados.add(new Object[]{
                    rs.getInt("idProdutos"),
                    rs.getString("nome"),
                    preco

                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

        ModeloTabela modelo = new ModeloTabela(dados, colunas);

        jTable_tabelaProdutos.setModel(modelo);
        jTable_tabelaProdutos.setFont(new java.awt.Font("Arial", 0, 16));

        jTable_tabelaProdutos.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTable_tabelaProdutos.getColumnModel().getColumn(0).setResizable(false);

        jTable_tabelaProdutos.getColumnModel().getColumn(1).setPreferredWidth(244);
        jTable_tabelaProdutos.getColumnModel().getColumn(1).setResizable(false);

        jTable_tabelaProdutos.getColumnModel().getColumn(2).setPreferredWidth(240);
        jTable_tabelaProdutos.getColumnModel().getColumn(2).setResizable(false);

        jTable_tabelaProdutos.getTableHeader().setReorderingAllowed(false);
        jTable_tabelaProdutos.setAutoResizeMode(jTable_tabelaProdutos.AUTO_RESIZE_ALL_COLUMNS);

        jTable_tabelaProdutos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        conexao.desconectar();

    }

